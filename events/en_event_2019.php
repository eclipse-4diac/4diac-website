<h1>Eclipse Insight: Cross Domain Engineering</h1>
<h2>What is an Eclipse Insight?</h2>
<p>Like an Eclipse DemoCamp, an Eclipse Insight is a collaborative event featuring technical talks and demonstrations from the Eclipse community. The topic of the event will be more focused on a certain technology area of the Eclipse ecosystem.</p>

<h2>Eclipse Insight: Cross Domain Engineering</h2>
<p>The software amount in all engineering domains is rapidly increasing as well as the complexity of today's software applications. To manage future software systems new engineering methods are required. This Eclipse Insight event addresses engineering methods in different domains, such as industrial automation, internet of things, and embedded systems. The goal of the event is to provide a technical overview and share experience knowledge about software engineering methods in different engineering domains. The demonstrations and talks will give an insight in <a href="https://eclipse.dev/4diac/">Eclipse 4diac</a>, <a href="https://projects.eclipse.org/projects/technology.basyx">Eclipse BaSyx</a>, <a href="https://af3.fortiss.org/">AutoFOCUS3</a> (provides the base for <a href="https://projects.eclipse.org/proposals/eclipse-systemfocus">Eclipse SystemFOCUS</a>), <a href="https://www.eclipse.org/hono/">Eclipse Hono</a>, <a href="https://www.eclipse.org/kura/">Eclipse Kura</a>, <a href="https://www.eclipse.org/kapua/">Eclipse Kapua</a>.</p>

