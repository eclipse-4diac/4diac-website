</p>

<p><img src="https://eclipse.dev/4diac/news/figs/LogiBUS_Logo.svg" alt="logiBUS&reg; Logo" width="125"/> is the Fusion of IEC 61499 programming and ISO 11783 compatibiltiy. 
</p>

<p>ISOBUS, also known as ISO 11783, is a communication protocol standard developed for agricultural equipment to facilitate the exchange of data between different types of farm machinery and implements. The term "ISOBUS" stands for "ISO Bus," where ISO refers to the International Organization for Standardization, and "Bus" refers to a system of communication between multiple devices.
</p>

<p>The goal of ISOBUS is to enable interoperability and compatibility among various agricultural devices from different manufacturers. This standard allows tractors, combines, sprayers, seeders, and other agricultural implements to communicate and share information seamlessly. This information exchange can include data such as equipment status, operational settings, and control commands.
</p>

<p>ISOBUS provides a standardized physical connection, usually implemented through a standard connector, and a set of communication protocols that allow different devices to exchange information. This can improve efficiency on the farm by enabling operators to control and monitor various implements from within the tractor cab, reducing the need for multiple displays and control interfaces.
</p>

<p>The ISOBUS standard defines different layers of communication, such as the physical layer (defining the connector and cabling), the data link layer (defining how data is packaged and transmitted), and the application layer (defining the structure and content of the data exchanged).
</p>

<p>In summary, ISOBUS (ISO 11783) is a standardized communication protocol used in the agricultural industry to enable different types of farm equipment to communicate and share data, ultimately leading to improved efficiency and ease of use for operators.
</p>


<p>
While exising ISOBUS Stacks require heavy C or C++ Programming as well as knowlege of both, the Bus Protocol and the Programming API of the corresponding ISOBUS Stack, 
logiBUS&reg; ease the same a lot by covering the Complexity in IEC 61499 Function Blocks. 
Therefore the Programming of a ISOBUS Universal Terminal Softkey Input in Eclipse 4diac™ is the same as programming a digital input. Nothing new to learn. 
</p>

