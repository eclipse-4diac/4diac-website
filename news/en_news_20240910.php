<h3>What to expect?</h3>
<p>
This winter school offers an in-depth exploration of IEC 61499 and Eclipse 4diac. 
Whether you are a newcomer eager to learn or a seasoned user looking to deepen your knowledge, this event is designed for every one of you.
</p>

<p>
At the 4days of Eclipse 4diac Winter School, we believe in &quot;no theory without hands-on experience.&quot; 
Thus, every session will be paired with practical workshops, allowing participants to immediately apply what they have learned in real-world scenarios.
</p>

<h3>Preliminary Program Overview:</h3>
<ul>
	<li>Classical Eclipse 4diac and IEC 61499 Topics:
		<ul>
    		<li><b>Developing IEC 61499 Solutions:</b> Learn the fundamentals of developing IEC 61499 automation solutions using Eclipse 4diac.</li>
    		<li><b>Advanced Concepts of IEC 61499:</b>  Dive into advanced features and methodologies for creating more complex and larger systems.</li>
        	<li><b>Fixing Your First Bug in Eclipse 4diac:</b>  A step-by-step guide to help new contributors get started with fixing bugs.</li>
    		<li><b>Extending 4diac IDE:</b>  Customize and extend the Eclipse 4diac IDE to suit your specific project needs.</li>
    		<li><b>Extending 4diac FORTE:</b>  Learn how to enhance and expand the capabilities of 4diac FORTE, the runtime environment for IEC 61499 applications.</li>
    		<li><b>Quick &amp; Easy Management of 4diac FORTE Builds with FBE:</b>  Simplify your 4diac FORTE builds and manage dependencies effortlessly using FBE.</li>
    		<li><b>Connecting Eclipse 4diac to Factory IO:</b>  A practical session on connecting 4diac FORTE with Factory IO for simulation and testing.</li>
    	</ul>
    </li>
	<li>New and Industry-Related Topics:
		<ul>	
    		<li><b>Programming UAO-based Devices with 4diac IDE:</b>  Explore how to program UAO-based devices using the 4diac IDE.</li>
    		<li><b>Industry Usage Reports:</b>  Hear directly from industry users about how they apply Eclipse 4diac in real-world settings.</li>
    		<li><b>Introduction to OPC UA:</b>  Learn the basics of OPC UA and how it can be used in conjunction with Eclipse 4diac to enhance industrial communication.</li>
    	</ul>
    </li>
	<li>Broader Open Source Discussions:
		<ul>	
    		<li><b>Women in Open Source:</b>  A session dedicated to promoting diversity and the inclusion of women in open-source communities.</li>
    		<li><b>Open Source: From Revolutionary Idea to Mainstream:</b>  A session from the Eclipse Foundation on the evolution and impact of open-source software over the years and how to get involved in an open source project.</li>
    	</ul>
    </li>
</ul>


