<p>We are very proud to announce the Eclipse 4diac 1.13.0 release!</p>

<p>With Eclipse 4diac 1.13.0 we provide you with a large set of improvements mostly in 4diac IDE. 
These improvements are, for example, a modernized look and feel of the ECC editor, editors and code generators for structured data types, 
advanced validation of basic function blocks, automatic layout of function block networks using the <a href="https://www.eclipse.org/elk/">Eclipse Layout Kernel</a>, 
and many many usability improvements. 
All these improvements should make your work with Eclipse 4diac much easier. 
A detailed list of new and noteworthy features can be found at the <a href="https://eclipse.dev/4diac/newandnoteworthy/1.13/">new and noteworthy page for the 1.13.0 release</a>.</p>

<p>We could address 190 issues in the work for the new Eclipse 4diac version. 
A full list of all bugs addressed in this release can be found on the <a href="https://projects.eclipse.org/projects/iot.4diac/releases/1.13.0/bugs">1.13.0 release page</a>.

<p>As always please download, test, and report any issues. For users of the previous releases a simple Help -> Check of Updates should upgrade you to 1.13.0.</p>

<p>Cheers,
	<br>The 4diac Team</p>
	
<p>PS: With Eclipse 4diac 1.13.0 the 4diac IDE requires at least Java 11 installed on your machine! 
If you download the official 4diac IDE package a correct Java version is already included.
Many thanks to the <a href="https://www.eclipse.org/justj/" >Eclipse JustJ project</a> for that support.</p>
